--  DDL for View DETALLE_FACTURA_COMPRA
--------------------------------------------------------

  CREATE OR REPLACE FORCE NONEDITIONABLE VIEW "CESAR"."DETALLE_FACTURA_COMPRA" ("ID", "IDENT_VENDEDOR", "NOM_VENDEDOR", "APE_VENDEDOR", "IDENT_CLIENTE", "NOM_CLIENTE", "APE_CLIENTE", "VEH_NUEVO", "VEH_USADO", "DESC_MARCA", "DESC_FABRICANTE", "ID_FABRICANTE", "DESC_ACCESORIO") AS 
  SELECT FA.ID, FA.ID_VENDEDOR AS IDENT_VENDEDOR, VE.NOMBRE AS NOM_VENDEDOR, VE.APELLIDO APE_VENDEDOR, FA.ID_CLIENTE AS IDENT_CLIENTE, 
CLI.NOMBRE AS NOM_CLIENTE, CLI.APELLIDO AS APE_CLIENTE, VN.ID AS VEH_NUEVO, FA.ID_VEHICULO_USADO AS VEH_USADO, MA.DESCRIPCION AS DESC_MARCA, 
FAB.DESCRIPCION AS DESC_FABRICANTE, PV.ID AS ID_FABRICANTE, PV.DESCRIPCION AS DESC_ACCESORIO
FROM FACTURAS FA INNER JOIN VENDEDORES VE ON VE.ID=ID_VENDEDOR
INNER JOIN CLIENTES CLI ON CLI.ID=FA.ID_CLIENTE
INNER JOIN VEHICULOS_NUEVOS VN ON FA.ID_VEHICULO_NUEVO= VN.ID
INNER JOIN MARCAS MA ON MA.ID=VN.ID_MARCA 
INNER JOIN FABRICANTES FAB ON FAB.ID=VN.ID_FABRICA
INNER JOIN DETALLE_FACTURAS DF ON FA.ID= DF.NRO_FACTURA
INNER JOIN PARTEVEHICULOS PV ON PV.ID=DF.COD_PARTE;
